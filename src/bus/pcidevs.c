uint16_t * pcidev_search_vendor(uint16_t vendor) {
	int length = sizeof(pci_vendor_ids) / sizeof(pci_vendor_ids[0]);
	for (int i = 0; i < length; i++) {
		if (pci_vendor_ids[i].vendor == vendor) {
			return pci_vendor_ids[i].name;
		}
	}
	return pci_vendor_ids[1].name;
}

uint16_t * pcidev_search_device(uint16_t vendor, uint16_t device) {
	int length = sizeof(pci_device_ids) / sizeof(pci_device_ids[0]);
	for (int i = 0; i < length; i++) {
		if (pci_device_ids[i].vendor == vendor && pci_device_ids[i].device == device) {
			return pci_device_ids[i].name;
		}
	}
	return pci_device_ids[1].name;
}
